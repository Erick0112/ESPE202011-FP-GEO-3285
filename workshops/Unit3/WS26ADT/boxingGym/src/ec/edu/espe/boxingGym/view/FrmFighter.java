    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Fighter fighter;
        String name;
        int age;
        float economicProfit;
        boolean seriousInjury;
        float weight;
        FighterController fighterController = new FighterController();
        
        name = txtName.getText();
        age = Integer.parseInt(txtAge.getText());
        economicProfit = Float.parseFloat(txtEconomicProfit.getText());
        seriousInjury = Boolean.getBoolean(cmbInjury.getSelectedItem().toString());
        weight = Float.parseFloat(txtWeight.getText());
        fighter = new Fighter(name, age, economicProfit, seriousInjury, weight);
        
        fighterController.save(fighter);
        JOptionPane.showMessageDialog(rootPane, fighter.getName());
        
 
    }//GEN-LAST:event_btnSaveActionPerformed

